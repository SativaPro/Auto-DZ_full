# функция input() всегда возвращает строку (текст)
# ввод производится в консоли после запуска скрипта

first_name = input("Введите ваше имя: ")        # запросит ввести имя
last_name = input("Введите вашу фамилию: ")     # запросит ввести фамилию

# f-страка выведет текст с значениями перемнных
print(f"Вас зовут: {last_name} {first_name}")
